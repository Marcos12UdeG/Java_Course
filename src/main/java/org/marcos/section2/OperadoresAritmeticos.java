package org.marcos.section2;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
       int i =5 , j=4;

        System.out.println("(i+j) = " + (i+j));
        System.out.println("(i-j) = " + (i-j));
        System.out.println("(i*j) = " + (i*j));
        System.out.println("(i/j) = " + (i/j));
    }
    


}
